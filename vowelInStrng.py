a=input("Enter a string:")
sum=0
for i in a:
    if(i=="a"):
        print("a")
        sum=sum+1
    if(i =="e"):
        print("e")
        sum=sum+1
    if(i =="i"):
        print("i")
        sum=sum+1
    if(i =="o"):
        print("o")
        sum=sum+1
    if(i =="u"):
        print("u")
        sum=sum+1
print("Number of vowels:", sum)
